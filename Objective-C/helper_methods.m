+ (NSString *)generateCreatedFromDateString:(NSString *)createdAtDateString {
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];
    dateFormatter.dateFormat = @"yyyy-MM-dd'T'HH:mm:ss.SSSZZ";
    dateFormatter.timeZone = [NSTimeZone defaultTimeZone];
    
    
    NSDate *tweetDate = [dateFormatter dateFromString:createdAtDateString];
    int timeBetween = (int)(tweetDate.timeIntervalSinceNow * -1);
    NSString *time;
    
    int minutes = timeBetween/60;
    int hours = minutes/60;
    int days = hours/24;
    
    if (days >= 1) {
        time = [NSString stringWithFormat:@"%dd", days];
    }
    else if (hours >= 1) {
        time = [NSString stringWithFormat:@"%dh", hours];
    }
    else if (minutes >= 1) {
        time = [NSString stringWithFormat:@"%dm", minutes];
    }
    else {
        time = [NSString stringWithFormat:@"%.0ds", timeBetween];
    }
    
    return time;
}

- (CAShapeLayer *)drawLineFromPoint:(CGPoint)fromPoint toPoint:(CGPoint)toPoint withColor:(UIColor *)color andLineWidth:(CGFloat)lineWidth {
    
    CAShapeLayer *lineShape = nil;
    CGMutablePathRef linePath = nil;
    
    linePath = CGPathCreateMutable();
    lineShape = [CAShapeLayer layer];
    
    lineShape.lineWidth = lineWidth;
    lineShape.strokeColor = color.CGColor;
    lineShape.transform = CATransform3DMakeRotation(30 * M_PI/180, 0.0, 0.0, 1.0);
    
    NSUInteger x = fromPoint.x;
    NSUInteger y = fromPoint.y;
    
    NSUInteger toX = toPoint.x;
    NSUInteger toY = toPoint.y;
    
    CGPathMoveToPoint(linePath, nil, x, y);
    CGPathAddLineToPoint(linePath, nil, toX, toY);
    
    lineShape.path = linePath;
    CGPathRelease(linePath);
    return lineShape;
}

